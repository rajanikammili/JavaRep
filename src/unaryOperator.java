
public class unaryOperator {

	public static void main(String[] args) {
		int a=10;
		int b=8;
						
		System.out.println("Post increment " + a++);//prints 'a' value and then increments -- Post increment
		System.out.println("Pre increment " + ++a);//increments first then prints 'a' value -- Pre increment
		
		System.out.println("Post decrement " + b--); //prints 'b' value and then decrements -- Post decrement
		System.out.println("Pre decrement " + --b);//decrements first then prints 'a' value -- Pre decrement
		
		
	}

}
